def plot_interactive_maps(graph_dict):

    maps = {}

    for name, G in graph_dict.items():

        # Convert the network graph into a GeoDataFrame
        nodes, edges = ox.graph_to_gdfs(G)

        # Set the CRS
        nodes = nodes.to_crs(epsg=4326)
        edges = edges.to_crs(epsg=4326)

        # Center the map
        center = [
            nodes.geometry.y.mean(),
            nodes.geometry.x.mean()
        ]

        # Use folium to create the map
        m = folium.Map(location=center, zoom_start=13, tiles="CartoDB positron")

        # Set the edges in folium
        folium.GeoJson(
            edges,
            style_function=lambda x: {
                "color": "#5DADE2",
                "weight": 2,
                "opacity": 0.6
            },
            name="Road Network"
        ).add_to(m)

        # Use a for loop to map the nodes
        for _, row in nodes.iterrows():
            folium.CircleMarker(
                location=[row.geometry.y, row.geometry.x],
                radius=1.5,
                color="black",
                fill=True,
                fill_opacity=0.7
            ).add_to(m)

        folium.LayerControl().add_to(m)

        maps[name] = m

    return maps

