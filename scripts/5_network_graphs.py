# Create a function to create a dictionary of networkx graphs using a bounding box
# The bounding box is based on the t2n file boundaries

def create_street_graph(gdf, name):

    # Compute Bounding Box
    minx, miny, maxx, maxy = gdf.total_bounds
    bbox = box(minx, miny, maxx, maxy)

    # Create street network graph
    G = ox.graph_from_polygon(
        bbox,
        network_type="drive"
    )

    # Store the name of the graph
    G.graph["name"] = name

    return G

def organize_street_graphs(gdf_dict):

    # Create the graphs dictionary
    graphs = {}

    # Loop through input dictionary
    for name, gdf in gdf_dict.items():
        graphs[name] = create_street_graph(gdf, name)

    return graphs

