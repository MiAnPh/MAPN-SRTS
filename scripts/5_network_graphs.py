def gdf_to_graph(edges_gdf):

    G = nx.Graph()

    for _, row in edges_gdf.iterrows():

        geom = row.geometry

        # handle LineString only (skip invalids safely)
        if geom is None:
            continue

        if geom.geom_type == "LineString":

            coords = list(geom.coords)

            u = coords[0]
            v = coords[-1]

            # add nodes with attributes
            G.add_node(u, x=u[0], y=u[1])
            G.add_node(v, x=v[0], y=v[1])

            # add edge attributes safely
            attr = row.drop("geometry").to_dict()
            G.add_edge(u, v, **attr)

        # If the geometry is a MultiLineString:
        elif geom.geom_type == "MultiLineString":

            for line in geom.geoms:
                coords = list(line.coords)

                u = coords[0]
                v = coords[-1]

                G.add_node(u, x=u[0], y=u[1])
                G.add_node(v, x=v[0], y=v[1])

                attr = row.drop("geometry").to_dict()
                G.add_edge(u, v, **attr)

    return G

