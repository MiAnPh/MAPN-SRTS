# get the shortest routes from each bus stop to a given school
def get_shortest_routes(school_name, school2stops, network_graph):
    paths = {}
    rows = school2stops[school_name]
    for route_num, row in rows:
        # approximate start and end point
        bus_x, bus_y = list(row['bus_coords'].coords)[0]
        orig = ox.distance.nearest_nodes(network_graph, bus_x, bus_y)
        school_x, school_y = list(row[school_name + '_coords'].coords)[0]
        dest = ox.distance.nearest_nodes(network_graph, school_x, school_y)
        # calculate route
        path = ox.routing.shortest_path(network_graph, orig, dest, weight="weight")

        paths[route_num] = path

    return paths



