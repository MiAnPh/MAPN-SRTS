def gdf_to_graph(edges_gdf):

    G = nx.MultiDiGraph()

    node_id_map = {}
    node_counter = 0

    def get_node_id(coord):

        nonlocal node_counter

        if coord not in node_id_map:
            node_id_map[coord] = node_counter
            G.add_node(
                node_counter,
                x=coord[0],
                y=coord[1]
            )
            node_counter += 1

        return node_id_map[coord]

    for _, row in edges_gdf.iterrows():

        geom = row.geometry

        if geom is None or geom.is_empty:
            continue

        if geom.geom_type == "LineString":
            lines = [geom]
        elif geom.geom_type == "MultiLineString":
            lines = list(geom.geoms)
        else:
            continue

        attr = row.drop("geometry").to_dict()
        attr = {
            k: str(v) if isinstance(v, (list, dict)) else v
            for k, v in attr.items()
        }
        attr['length'] = geom.length

        for line in lines:

            coords = list(line.coords)
            if len(coords) < 2:
                continue

            u = get_node_id(coords[0])
            v = get_node_id(coords[-1])

            G.add_edge(u, v, **attr)
            G.add_edge(v, u, **attr)

    G.graph["crs"] = "EPSG:4326"
    G.graph["simplified"] = True

    return G


