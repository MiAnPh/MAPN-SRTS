def fill_sidewalk_gaps_along_path(path, # list of nodes in the path, e.g. [20,21,24,33,50]
                                  street_graph,
                                  sidewalk_graph):
    '''
    runs along the path and checks on either side for sidewalks, notes where there are gaps and adds "phantom edges"
    '''
    # index along path
    i = 1
    while i < len(path):
        u = path[i-1] # from
        v = path[i] # to
        # get the linestring for this path segment
        line = street_graph.loc[(street_graph['from'] == u) & (street_graph['to'] == v)].iloc[0].geometry
        # get the direction of this line, as an angle between 0 and 360
        direction = get_direction(line)
        # these are how we iterate over this line
        distance = 0
        current_point = Point([0,0])
        current_edge1 = None
        current_edge2 = None
        # paramaters to play with regarding searching
        search_width = 3 # currently arbitrarily set, determines how frequently we check for sidewalks, smaller number means more frequent
        search_dist = 15 # currently arbitrarily set, determines how far to look for a sidewalk
        gap_close_width = 15 # currently arbitrarily set, determine how generous of an angle to look for a connecting sidewalk and street
        gap_close_dist = 30 # currently arbitrarily set, determine how long of a gap we are willing to close
        while distance < line.length:
            # create a point along the line as the origin for the sidewalk search
            distance += search_width
            current_point = line.interpolate(distance)
            # set the two directions to search, perpendicular to this line's direction
            search_d1 = (direction + 90) % 360
            search_d2 = (direction - 90) % 360
            search_box1 = draw_line(current_point, search_d1, search_dist).buffer(search_width, cap_style="flat")
            search_box2 = draw_line(current_point, search_d2, search_dist).buffer(search_width, cap_style="flat")
            # look for sidewalk edges in the search boxes
            sb1df = gpd.GeoDataFrame(geometry=[search_box1],crs="3857") # set up the space for a spatial join
