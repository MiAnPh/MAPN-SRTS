# edit the stops dataframe with the distances to each school, and get the closest stop to each school per route
def get_distances_to_schools(schools_gdf, input_stops_gdf):

    stops_gdf = input_stops_gdf.copy()
    # Run a for loop on the schools with their locations
    for school_name, school_coords, school_geom in zip(schools_gdf["SchoolName"], schools_gdf["school_coords"], schools_gdf.geometry):

        # Add the column with the school_name that is the distance to that school
        stops_gdf[school_name] = stops_gdf.geometry.distance(school_geom)
        stops_gdf[school_name + "_coords"] = school_coords

    # get individual routes
    routes_dict = {}

    for index, row in stops_gdf.iterrows():

        routes = str(row["ROUTE"]).split()

        for route in routes:
            # Initialize list if route not yet in dictionary
            if route not in routes_dict:
                routes_dict[route] = []

            # Add stop
            routes_dict[route].append(row)

    # make a dictionary from school to its nearest stops for each route
    school2stops = {}

    for school in schools_gdf["SchoolName"]:
        if not school in school2stops:
            school2stops[school] = []

        for k, v in routes_dict.items():
            v = sorted(v, key = lambda row: row[school], reverse = False)
            school2stops[school].append([k, v[0]])

    return school2stops

