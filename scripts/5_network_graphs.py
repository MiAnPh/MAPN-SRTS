# Add weights based on f_types and speed limit

def weight_edges(graph, speed_limit_df):
    graph = ox.distance.add_edge_lengths(graph)

    for u, v, _, data in graph.edges(keys=True, data=True):

        # Set facility type weight variable
        data["f_type_w"] = None

        f_type = data.get("f_type")

        # Assign facility type weights
        if f_type in [
            "sidewalk_plus",
            "sidewalk",
            "sidewalk_connection",
            "sidewalk_connection_plus",
            "phantom_sidewalk"
        ]:
            data["f_type_w"] = 1

        elif f_type in [
            "crosswalk",
            "crosswalk_plus",
            "phantom_crosswalk",
        ]:
            u_coords = graph.nodes[u].values()
            v_coords = graph.nodes[v].values()
            speed_limit = get_speed_limit(LineString([u_coords,v_coords]), speed_limit_df)
            # print(speed_limit)
            data["f_type_w"] = 1.3 + ((speed_limit-25)/50) # Setting the crosswalk weights to 1.3 so it takes 30% longer for crossing
            # print(data["f_type_w"])
        elif f_type in [
            "phantom_midblock",
        ]:
            data["f_type_w"] = 1.05
        data['weight'] = data['length']**data['f_type_w']
    return graph


