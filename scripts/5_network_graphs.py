def get_direction(line):
    p0, p1 = line.coords[:][0], line.coords[:][-1]
    dx = p1[0] - p0[0]
    dy = p1[1] - p0[1]
    rads = math.atan2(dy, dx)
    # Convert to degrees
    degrees = math.degrees(rads)
    # Optional: Normalize to [0, 360)
    return degrees if degrees >= 0 else degrees + 360

def draw_line(point, angle_deg, distance):
    # Convert angle to radians
    angle_rad = math.radians(angle_deg)
    # Calculate end point coordinates
    x1, y1 = point.coords[:][0]
    x2 = x1 + distance * math.cos(angle_rad)
    y2 = y1 + distance * math.sin(angle_rad)
    # Create and return line
    return LineString([point, Point([x2, y2])])

